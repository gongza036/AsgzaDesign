package com.app.gongza.asgzdesign.fragments.action;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

public class NewsFragmentPagerAdapter extends FragmentPagerAdapter {

    private List<NewsScrollAbleFragment> fragmentList;
    private List<String> titleList;

    public NewsFragmentPagerAdapter(FragmentManager fm, List<NewsScrollAbleFragment> fragmentList, List<String> titleList) {
        super(fm);
        this.fragmentList = fragmentList;
        this.titleList = titleList;
    }

    @Override
    public Fragment getItem(int position) {
        return fragmentList.get(position);
    }

    @Override
    public int getCount() {
        return fragmentList.size();
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return titleList.get(position);
    }

}
